    /**
     * Will first generate an array of items deemend safe (won't trigger game restart). 
     * If the boolean safety stays false. Then the gameover dialog window (endingDialog) will show
     * If the item being checked is safe. The information window will show.
     * This serves as a way of opening chest and obtaining the keys from their perspective chest.
     *@author Ernesto Rodriguez
     */
    private void checkItemButtonActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_checkItemButtonActionPerformed
    {//GEN-HEADEREND:event_checkItemButtonActionPerformed
        if (backpack.getSelectedValue() != null)
        {
            String[] safeItems = {  "Storageroom Chest", "Key #1", "Attic Chest", "Key #2", "Refrigerator", "Dusty recipe box", "Broom",
                                    "Mirror", "Shower", "Rocking chair", "Window", "Doll House", "Intricate Oil Lamp", "Candelabra"};
            boolean safety = false;
            int i = 0;
            while (i < safeItems.length)
            {
                if (backpack.getSelectedValue().equals(safeItems[i]))
                {
                    safety = true;
                    break;
                }
                else
                    safety = false;
                i++;
            }

            if (safety)
            {
                controls.explore_items(backpack.getSelectedValue(), infoTextBag, infoTextEffect);
                infoDialog.setVisible(true);
            }
            else
            {
                controls.explore_items(backpack.getSelectedValue(), endingTextBag, endingTextEffect);
                endingDialog.setVisible(true);  
            }
        }
    }//GEN-LAST:event_checkItemButtonActionPerformed

